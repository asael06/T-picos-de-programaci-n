    public void cargarDatos(){
        DefaultTableModel modelo=(DefaultTableModel)tblLugarTuristico.getModel();
        LibreriaConexion libreria=new LibreriaConexion("localhost", "5432", "guiaturistica", "postgres", "123456789");
        libreria.conectarBaseDatos();
        try {
            lista=libreria.mostrarLugar();
        } catch (SQLException ex) {
            Logger.getLogger(LugarTuristico.class.getName()).log(Level.SEVERE, null, ex);
        }
        lista.forEach((lugar) -> {
            modelo.addRow(lugar);
        });
    }

